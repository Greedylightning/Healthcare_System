    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = JTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        WorkRequest request = (WorkRequest) JTable.getValueAt(selectedRow, 2);
        request.setReceiver(account);
        request.setStatus("Pending");
        populateTable();
    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = JTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        LabTestWorkRequest request = (LabTestWorkRequest) JTable.getValueAt(selectedRow, 2);
        String result = resultJTextField.getText().trim();
        if(result.length() > 30){
            JOptionPane.showMessageDialog(null, "Result is too long!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        request.setTestResult(result);
        request.setResolveDate(new Date());
        request.setStatus("Completed");
        resultJTextField.setText("");
        populateTable();
    }//GEN-LAST:event_processJButtonActionPerformed


