    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)JTable.getModel();
        model.setRowCount(0);
         for(WorkRequest request : organization.getWorkQueue().getWorkRequestCatalog()){
            Object[] row = new Object[6];
            row[0] = format.format(request.getRequestDate());
            if(request.getResolveDate() == null){
                row[1] = null;
            }
            else{
                row[1] = format.format(request.getResolveDate());
            }
            row[2] = request;
            row[3] = request.getSender().getEmployee().getName();
            row[4] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[5] = request.getStatus();
            
            model.addRow(row);
        }
    }
