    public void populateTestTable(){
        DefaultTableModel model = (DefaultTableModel) testJTable.getModel();
        model.setRowCount(0);
        for(WorkRequest request : account.getWorkQueue().getWorkRequestCatalog()){
            Object[] row = new Object[6];
            row[0] = format.format(request.getRequestDate());
            if(request.getResolveDate() == null){
                row[1] = null;
            }
            else{
                row[1] = format.format(request.getResolveDate());
            }
            row[2] = request;
            row[3] = request.getReceiver();
            row[4] = request.getStatus();
            String result = ((LabTestWorkRequest) request).getTestResult();
            row[5] = result == null ? "Waiting" : result;
            model.addRow(row);
        }
    }
    
    public void populatePatientTable(){
        DefaultTableModel model = (DefaultTableModel) patientJTable.getModel();
        model.setRowCount(0);
        for(WorkRequest workRequest : organization.getWorkQueue().getWorkRequestCatalog()){
                Object[] row = new Object[2];
                row[0] = workRequest.getSender().getEmployee().getName();
                row[1] = format.format(workRequest.getRequestDate());
                model.addRow(row);
        }
    }
