    private void requestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestJButtonActionPerformed
        // TODO add your handling code here:
        String message = testMessageJTextField.getText();
        
        if(message == null){
            JOptionPane.showMessageDialog(null, "Message Should Not Be Empty!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        LabTestWorkRequest request = new LabTestWorkRequest();
        request.setMessage(message);
        request.setSender(account);
        request.setStatus("Sent");
        request.setRequestDate(new Date());
        
        Organization organization = null;
        for(Organization o : enterprise.getOrganizationCatalog().getOrganizationCatalog()){
            if(o instanceof AssistantOrganization){
                organization = o;
                break;
            }
        }
        if(organization != null){
            organization.getWorkQueue().getWorkRequestCatalog().add(request);
            account.getWorkQueue().getWorkRequestCatalog().add(request);
        }
        populateTestTable();
        testMessageJTextField.setText("");
    }//GEN-LAST:event_requestJButtonActionPerformed

    private void sendResultJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendResultJButtonActionPerformed
        // TODO add your handling code here:
        //SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String message = patientMessageJTextField.getText();
        if(message == null){
            JOptionPane.showMessageDialog(null, "Message Should Not Be Empty!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        WorkRequest request = new WorkRequest();
        request.setMessage(message);
        request.setSender(account);
        request.setResolveDate(new Date());
        
        
        int selectedPatient = patientJTable.getSelectedRow();
        if(selectedPatient < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String pname = (String) patientJTable.getValueAt(selectedPatient, 0);
        enterprise.getSelectedPatient(pname).getWorkQueue().getWorkRequestCatalog().add(request);
        populatePatientTable();
        JOptionPane.showMessageDialog(null, "Result Saved!!", "Warning", JOptionPane.WARNING_MESSAGE);
        patientMessageJTextField.setText("");
    }//GEN-LAST:event_sendResultJButtonActionPerformed

