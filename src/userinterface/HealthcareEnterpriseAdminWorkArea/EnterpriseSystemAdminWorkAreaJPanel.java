    public void populateTree(){
        DefaultTreeModel model = (DefaultTreeModel) JTree.getModel();
        JTree.setRootVisible(false);
        ArrayList<Organization> organizationCatalog = enterprise.getOrganizationCatalog().getOrganizationCatalog();
        
        Organization organization;
        
        DefaultMutableTreeNode root= (DefaultMutableTreeNode)model.getRoot();
        root.removeAllChildren();
        
        DefaultMutableTreeNode organizationNodes;
        
        for(int i = 0; i < organizationCatalog.size(); i++){
            organization = organizationCatalog.get(i);
            organizationNodes = new DefaultMutableTreeNode(organization.getName());
            root.insert(organizationNodes, i);
        }
        model.reload();
    }
    
