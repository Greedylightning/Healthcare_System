    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        Organization organization = (Organization) organizationNameJComboBox.getSelectedItem();
        HealthcareRoleType type = (HealthcareRoleType) employeeRoleJComboBox.getSelectedItem();

        String name = nameJTextField.getText().trim();
        String username = usernameJTextField.getText().trim();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        
        if(!name.matches("[a-zA-Z]+")){
            JOptionPane.showMessageDialog(null, "Invalid employee name!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Employee employee = organization.getEmployeeCatalog().createEmployee(name);
        if(type.equals(HealthcareRoleType.Collector)){
            UserAccount account = organization.getUserAccountCatalog().createUserAccount(username, password, employee, new CollectorRole());
        }
        else if(type.equals(HealthcareRoleType.Analyst)){
            UserAccount account = organization.getUserAccountCatalog().createUserAccount(username, password, employee, new AnalystRole());
        }
        else{
            ;
        }
        populateTable();
        nameJTextField.setText("");
        usernameJTextField.setText("");
        passwordJPasswordField.setText("");
    }//GEN-LAST:event_submitJButtonActionPerformed

