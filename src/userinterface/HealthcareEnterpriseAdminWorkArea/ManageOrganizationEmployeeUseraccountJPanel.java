    public void populateComboBox(){
        organizationNameJComboBox.removeAllItems();
        employeeRoleJComboBox.removeAllItems();
        for(Organization organization : enterprise.getOrganizationCatalog().getOrganizationCatalog()){
            organizationNameJComboBox.addItem(organization);
        }
    }
    
    public void populateRoleBox(){
        employeeRoleJComboBox.removeAllItems();
        if(enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Healthcare_Community)){
            for(HealthcareRoleType type : Role.HealthcareRoleType.values()){
                if(!type.equals(Role.HealthcareRoleType.HealthcareManager)) employeeRoleJComboBox.addItem(type);
            }
        }
    }
    
    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) JTable.getModel();
        model.setRowCount(0);
        for(Organization organization : enterprise.getOrganizationCatalog().getOrganizationCatalog()){
            for(UserAccount userAccount : organization.getUserAccountCatalog().getUserAccountCatalog()){
                Object[] row = new Object[3];
                row[0] = organization.getName();
                row[1] = userAccount.getEmployee().getName();
                row[2] = userAccount.getRole();
                model.addRow(row);
            }
        }
    }
    
