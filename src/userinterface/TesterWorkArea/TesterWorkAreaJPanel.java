    public void populateTestTable(){
        DefaultTableModel model = (DefaultTableModel)testJTable.getModel();
        model.setRowCount(0);
        for(WorkRequest request : organization.getWorkQueue().getWorkRequestCatalog()){
            Object[] row = new Object[9];
            row[0] = format.format(request.getRequestDate());
            if(request.getResolveDate() == null){
                row[1] = null;
            }
            else{
                row[1] = format.format(request.getResolveDate());
            }
            row[2] = request.getSender().getEmployee().getName();
            row[3] = ((MedicineTestWorkRequest) request).getMedicine().getId();
            row[4] = ((MedicineTestWorkRequest) request);
            row[5] = ((MedicineTestWorkRequest) request).getMedicine().getDescription();
            row[6] = request.getStatus();
            row[7] = ((MedicineTestWorkRequest) request).getResult();
            row[8] = ((MedicineTestWorkRequest) request).getComment();
            model.addRow(row);
        }
    }
