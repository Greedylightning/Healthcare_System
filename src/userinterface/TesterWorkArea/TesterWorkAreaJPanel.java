        // TODO add your handling code here:
        int selectedRow = testJTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        MedicineTestWorkRequest request = (MedicineTestWorkRequest) testJTable.getValueAt(selectedRow, 4);
        request.setResult((String) testSignJComboBox.getSelectedItem());
        request.setComment(resultJTextField.getText());
        request.setStatus("Complete");
        request.setResolveDate(new Date());
        resultJTextField.setText("");
        populateTestTable();
