    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) JTable.getModel();
        model.setRowCount(0);
        for(Enterprise enterprise : network.getEnterpriseCatalog().getEnterpriseCatalog()){
            for(Employee employee : enterprise.getEmployeeCatalog().getEmployeeCatalog()){
                Object[] row = new Object[3];
                row[0] = enterprise.getName();
                row[1] = enterprise.getEnterpriseType();
                row[2] = employee.getName();
                model.addRow(row);
            }
        }
    }
    
