    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = JTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String enterpriseName = (String) JTable.getValueAt(selectedRow, 0);
        String employeeName = (String) JTable.getValueAt(selectedRow, 2);
        UserAccount ua = null;
        for(Enterprise enterprise : network.getEnterpriseCatalog().getEnterpriseCatalog()){
            if(enterprise.getName().equals(enterpriseName)){
                for(UserAccount useraccount : enterprise.getUserAccountCatalog().getUserAccountCatalog()){
                    if(useraccount.getEmployee().getName().equals(employeeName)){
                        ua = useraccount;
                        break;
                    }
                }
            }
            if(ua != null){
                break;
            }
        }
        for(Enterprise enterprise : network.getEnterpriseCatalog().getEnterpriseCatalog()){
            if(enterprise.getName().equals(enterpriseName)){
                enterprise.getEmployeeCatalog().getEmployeeCatalog().remove(ua.getEmployee());
                enterprise.getUserAccountCatalog().getUserAccountCatalog().remove(ua);
                break;
            }
        }
        populateTable();
    }//GEN-LAST:event_removeJButtonActionPerformed


