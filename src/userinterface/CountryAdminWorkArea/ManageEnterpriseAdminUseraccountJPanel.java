    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        Enterprise enterprise = (Enterprise) enterpriseNameJComboBox.getSelectedItem();

        String name = nameJTextField.getText().trim();
        String username = usernameJTextField.getText();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        if(!name.matches("[a-zA-Z]+")){
            JOptionPane.showMessageDialog(null, "Invalid employee name!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Employee employee = enterprise.getEmployeeCatalog().createEmployee(name);
        
        if(enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Hospital)){
            UserAccount account = enterprise.getUserAccountCatalog().createUserAccount(username, password, employee, new HospitalEnterpriseSystemAdminRole());
        }
        else if(enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Medicine_Company)){
            UserAccount account = enterprise.getUserAccountCatalog().createUserAccount(username, password, employee, new MedicineEnterpriseSystemAdminRole());
        }
        else if(enterprise.getEnterpriseType().equals(Enterprise.EnterpriseType.Healthcare_Community)){
            UserAccount account = enterprise.getUserAccountCatalog().createUserAccount(username, password, employee, new HealthcareEnterpriseSystemAdminRole());
        }
        populateTable();
        nameJTextField.setText("");
        usernameJTextField.setText("");
        passwordJPasswordField.setText("");
    }//GEN-LAST:event_submitJButtonActionPerformed

