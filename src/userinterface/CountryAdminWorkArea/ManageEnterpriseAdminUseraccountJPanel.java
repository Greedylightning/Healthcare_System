    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) JTable.getModel();
        model.setRowCount(0);
        for(Enterprise enterprise : network.getEnterpriseCatalog().getEnterpriseCatalog()){
            for(UserAccount userAccount : enterprise.getUserAccountCatalog().getUserAccountCatalog()){
                Object[] row = new Object[3];
                row[0] = enterprise.getName();
                row[1] = enterprise.getEnterpriseType();
                row[2] = userAccount.getUsername();
                model.addRow(row);
            }
        }
    }
    
    public void populateComboBox(){
        enterpriseNameJComboBox.removeAllItems();
        for(Enterprise enterprise : network.getEnterpriseCatalog().getEnterpriseCatalog()){
            enterpriseNameJComboBox.addItem(enterprise);
        }
    }
